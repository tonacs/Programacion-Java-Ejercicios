package calculadora;
public class Operaciones {
    public static int suma(int a,int b){
        return a+b;
    }
    public static int resta(int a,int b){
        return a-b;
    }
    public static int division(int a,int b){
        return a/b;
    }
    public static int multiplicar(int a,int b){
        return a*b;
    }
}
